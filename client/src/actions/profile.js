import axios from "axios";
import {
  CLEAR_PROFILE,
  DELETE_ACC,
  GET_PROFILE,
  GET_PROFILES,
  GITHUB_REPOS,
  PROFILE_ERROR,
  UPDATE_PROFILE,
} from "../actions/types";
import { setAlert } from "../actions/alert";

export const getCurrentProfile = () => async (dispatch) => {
  try {
    const res = await axios.get("/api/profile/me");

    dispatch({
      type: GET_PROFILE,
      payload: res.data,
    });
  } catch (err) {
    dispatch({
      type: PROFILE_ERROR,
      payload: {
        msg: err.response.statusText,
        status: err.response.status,
      },
    });
  }
};

